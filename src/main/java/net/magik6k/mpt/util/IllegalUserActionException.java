package net.magik6k.mpt.util;

public class IllegalUserActionException extends Exception{

	private static final long serialVersionUID = -8943099301208854126L;

	public IllegalUserActionException() { super(); }
	public IllegalUserActionException(String message) { super(message); }
	public IllegalUserActionException(String message, Throwable cause) { super(message, cause); }
	public IllegalUserActionException(Throwable cause) { super(cause); }
	
}
